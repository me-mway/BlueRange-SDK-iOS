#import "BRTimeFormatter.h"

@implementation BRTimeFormatter

+ (NSString*) relativeDateTimeString:(NSDate*)time {
    NSDateFormatter *df = [[NSDateFormatter alloc] init];
    [df setFormatterBehavior:NSDateFormatterBehavior10_4];
    [df setDateFormat:@"EEE, dd MMM yy HH:mm:ss VVVV"];
    NSDate *todayDate = [NSDate date];
    double ti = [time timeIntervalSinceDate:todayDate];
    ti = ti * -1;
    if (ti < 0) {
        int diff = round(ti);
        return [NSString stringWithFormat:@"In %d seconds", -diff];
    } else if (ti < 60) {
        int diff = round(ti);
        return [NSString stringWithFormat:@"%d seconds ago", diff];
    } else if (ti < 3600) {
        int diff = round(ti / 60);
        return [NSString stringWithFormat:@"%d minutes ago", diff];
    } else if (ti < 86400) {
        int diff = round(ti / 60 / 60);
        return[NSString stringWithFormat:@"%d hours ago", diff];
    } else if (ti < 2629743) {
        int diff = round(ti / 60 / 60 / 24);
        return[NSString stringWithFormat:@"%d days ago", diff];
    } else {
        return @"never";
    }	
}

@end
